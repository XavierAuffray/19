#include "../libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t x)
{
	unsigned char	*ss1;
	unsigned char	*ss2;

	ss1 = (unsigned char *)s1;
	ss2 = (unsigned char *)s2;
	if (x == 0)
		return (0);
	while (x != 1 && *ss1 == *ss2)
	{
		ss1++;
		ss2++;
		x--;
	}
	if (*ss1 == *ss2)
		return (0);
	return (*ss1 - *ss2);
}
